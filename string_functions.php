<?php

// Common PHP String Functions!
// strlen() , str_word_count() , strrev() , strpos() , str_replace() 

$str = "php php dog cat";


// strlen
strlen( $str );

// str_word_count
str_word_count($str);

// strrev
strrev($str);

// strpos
strpos($str, "cat" );

// str_replace
str_replace("cat", "dog", $str);

